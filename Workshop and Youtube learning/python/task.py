def add_numbers(n1, n2):
    return n1 + n2

def multiply_numbers(n1, n2):
    return n1 * n2

number1 = 5
number2 = 30

sum_result = add_numbers(number1, number2)
print("The sum is",sum_result)

product_result = multiply_numbers(number1, number2)
print("The product is",product_result)
